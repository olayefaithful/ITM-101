function subtractNumbers(a, b) {
  return a - b;
}

// Multiply two numbers
function multiplyNumbers(a, b) {
  return a * b;
}

// Divide two numbers
function divideNumbers(a, b) {
  return a / b;
}

// Exponentiation (a raised to the power of b)
function exponentNumbers(a, b) {
  return a ** b;
}

